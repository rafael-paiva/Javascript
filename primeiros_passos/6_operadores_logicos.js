var inputOne = 'VALUE_A', inputTwo = 'VALUE_B';

/* AND && */
    var resultAND = inputOne === 'VALUE_A'  && inputTwo === 'VALUE_B';   //Condição 1 satisfeita E condição 2 satisfeita
    console.log(resultAND);
/* OR || */
    var resultOR = inputOne === 'VALUE_A' || inputTwo === 'VALUE_B';     //Condição 1 satisfeita OU condição 2 satisfeita
    console.log(resultOR);
/* NOT != */
    var resultNOT = inputOne != 'VALUE_A' && inputTwo != 'VALUE_B';      //Valor 1 diferente de 'VALUE_A' E Valor 2 diferente de 'VALUE_B'
    console.log(resultNOT);
