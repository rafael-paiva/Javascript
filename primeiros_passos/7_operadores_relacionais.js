
/*
    Ação                            Operador
    Igual a                         ==
    Diferente de                    !=
    Maior que                       >
    Menor que                       <
    Maior ou igual a                >=
    Menor ou igual a                <=
    Igual e do mesmo tipo que       ===
    Diferente e de outro tipo que   !==
*/
var n1 = 5, n2 = 8;

console.log(n1 == n2);  //false
console.log(n1 != n2);  //true
console.log(n1 > n2);   //false
console.log(n1 < n2);   //true
console.log(n1 >= n2);  //false
console.log(n1 <= n2);  //true
console.log(n1 === n2); //false
console.log(n1 !== n2); //true
